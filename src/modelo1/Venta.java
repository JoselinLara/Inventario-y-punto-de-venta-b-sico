    private void jButtonVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVentaActionPerformed
         float ve;
         int cantidad;//cantidad  para  vender
         int cantidad2;//cantidad en inventario
         int cantidadR;//cantidad restante
         String  tmp;
         String total;
         String temp;
         String tempo;
         temp=jTextFieldCanti.getText();
         cantidad=Integer.parseInt(temp);
         tmp=productFound.getCanti();
         cantidad2=Integer.parseInt(tmp);
         ve=Tienda.ventaProducto(productFound,cantidad);
        total=Float.toString(ve);
        if(temp.endsWith(tmp)){
           productFound.setCantidad(null);
           productFound.setDisponible(false);
        }else if(cantidad2>cantidad){
            cantidadR=cantidad2-cantidad;
            tempo=Integer.toString(cantidadR);
            productFound.setCantidad(tempo);
        }
        if(ve>0){
            jLabelTotal.setText(total);
            jTextFieldEfectivo.setEnabled(true);
            jButtonCam.setEnabled(true);
            
        }  else{
            actualizaEstado("Error.Cantidad de  productos no valida");
        }
    }//GEN-LAST:event_jButtonVentaActionPerformed

