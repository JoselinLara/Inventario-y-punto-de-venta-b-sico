     public void despliegaDatosLibro(Producto p){
        
        jTextFieldNombre.setText(p.getNombre());
        jLabelCaducidad.setText(p.getCaducidad());
        jLabelPrecio.setText(p.getPrecio());
       
        if (p.isDisponible())
            jLabelDisponible.setText("Disponible");
        else{
            jLabelDisponible.setText("No disponible");
        }
    }
