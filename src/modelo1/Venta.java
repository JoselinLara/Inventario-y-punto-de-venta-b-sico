    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        String clv = jTextFieldClave.getText();
        productFound = Tienda.buscarP(clv);
        if(productFound.getCanti() != null) {
            despliegaDatosLibro(productFound);
            
            if (productFound.getCanti()!=null){
                actualizaEstado("Producto(s) encontrado(s).");
               jButtonVenta.setEnabled(true);
            }
            else{
                actualizaEstado("El producto(s) solicitado no esta en el inventario");
                jButtonVenta.setEnabled(false);
                jButtonCam.setEnabled(false);
            }
        }else
            actualizaEstado("No se encontro el(los) producto(s)");
        Tienda.mostrarInventario();
    }//GEN-LAST:event_jButtonBuscarActionPerformed

