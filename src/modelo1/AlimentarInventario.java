    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
      boolean gu=Tienda.guardarProducto();
      
      if(gu==false){
           JOptionPane.showOptionDialog(this, "Error, Los datos no han podido  guardar.",
                   null,JOptionPane.ERROR_MESSAGE,JOptionPane.ERROR_MESSAGE,
                    null,new Object[]{"OK"},"OK");
      }
      else{
            JOptionPane.showOptionDialog(this,"Los datos han sido guardados exitosamente.",null,
                     JOptionPane.INFORMATION_MESSAGE, JOptionPane.INFORMATION_MESSAGE,
                    null,new Object[]{"Aceptar"},"Aceptar");
      }
    }//GEN-LAST:event_jButtonGuardarActionPerformed

