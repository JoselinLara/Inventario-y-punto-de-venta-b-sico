    private void jButtonAltaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAltaActionPerformed
        String tempo=jTextFieldCantidad.getText();//cantidad
        String clave= jTextFieldClave.getText();
        String nombre= jTextFieldNombre.getText();
        String tmp=jTextFieldPrecio.getText();//precio
        String caducidad=jTextFieldCaducidad.getText();
       
        if(!tempo.isEmpty()&&!clave.isEmpty()&&!nombre.isEmpty()&&!tmp.isEmpty()&&!caducidad.isEmpty()){
            Producto p =new Producto(tempo,clave,nombre,tmp,caducidad);
            Tienda.alimentaInventario(p);
            Tienda.mostrarInventario(); 
            JOptionPane.showOptionDialog(this,"Los datos se han añadido exitosamente.",null,
                     JOptionPane.INFORMATION_MESSAGE, JOptionPane.INFORMATION_MESSAGE,
                    null,new Object[]{"Aceptar"},"Aceptar");
         
        }else 
            JOptionPane.showOptionDialog(this, "Error,Datos incompletos",null,JOptionPane.ERROR_MESSAGE,JOptionPane.ERROR_MESSAGE,
                    null,new Object[]{"OK"},"OK");
        
    }//GEN-LAST:event_jButtonAltaActionPerformed

